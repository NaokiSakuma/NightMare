#include "Press.h"

//----------------------------------------------------------------------
//! @brief コンストラクタ
//!
//! @param[in] x座標、y座標
//----------------------------------------------------------------------
Press::Press(int pos_x, int pos_y)
	:m_count(0)			//カウント
{
	m_handle = new Texture(L"Resources\\Images\\Press.png");	//画像
	m_pos_x = pos_x;	//x座標
	m_pos_y = pos_y;	//y座標
	m_grp_x = 0;		//元画像のgrp_x
	m_grp_y = 0;		//元画像のgrp_y
	m_grp_w = 64;		//元画像のgrp_w
	m_grp_h = 288;		//元画像のgrp_h
}

//----------------------------------------------------------------------
//! @brief デストラクタ
//----------------------------------------------------------------------
Press::~Press()
{
	delete m_handle;	//画像
}

//----------------------------------------------------------------------
//! @brief 移動
//!
//! @param[in] なし
//!
//! @return なし
//----------------------------------------------------------------------
void Press::Move()
{
	m_count++;
	//下移動
	if (m_count < 60)
		m_spd_y = 1;
	//上移動
	if (m_count > 60)
		m_spd_y = -1;
	//reset
	if (m_count > 120)
		m_count = 0;

}