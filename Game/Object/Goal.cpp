#include "Goal.h"

//----------------------------------------------------------------------
//! @brief コンストラクタ
//!
//! @param[in] なし
//----------------------------------------------------------------------
Goal::Goal(int pos_x, int pos_y)
{
	m_handle = new Texture(L"Resources\\Images\\goal.png");	//画像
	m_pos_x = pos_x;			//x座標
	m_pos_y = pos_y;			//y座標
	m_grp_x = 0;				//元画像のgrp_x
	m_grp_y = 0;				//元画像のgrp_y
	m_grp_w = GOAL_GRP_W;		//元画像のgrp_w
	m_grp_h = GOAL_GRP_H;		//元画像のgrp_h
	m_state = false;
}

//----------------------------------------------------------------------
//! @brief デストラクタ
//----------------------------------------------------------------------
Goal::~Goal()
{
	delete m_handle;
	m_handle = nullptr;
}