//----------------------------------------------------------------------
//! @brief コンストラクタ
//!
//! @param[in] なし
//----------------------------------------------------------------------
Camera::Camera()
{
	//m_pos_x = SCREEN_WIDTH / 2;
	//m_pos_y = SCREEN_HEIGHT / 2;
	m_pos_x = 0;	//x座標
	m_pos_y = 0;	//y座標
}
//----------------------------------------------------------------------
//! @brief デストラクタ
//----------------------------------------------------------------------
Camera::~Camera()
{

}

//----------------------------------------------------------------------
//! @brief カメラの座標変更
//!
//! @param[in] x座標、幅
//!
//! @return なし
//----------------------------------------------------------------------
void Camera::Coordinate(int pos_x, int grp_w)
{
	m_pos_x = pos_x + (grp_w / 2);	
}

//----------------------------------------------------------------------
//! @brief スクロールの限界
//!
//! @param[in] なし
//!
//! @return なし
//----------------------------------------------------------------------
void Camera::CameraMaxPos()
{
	if (m_pos_x < SCREEN_WIDTH / 2)
	{
		m_pos_x = SCREEN_WIDTH / 2;
	}
	if (m_pos_x >  32 * 90 - 320)
	{
		m_pos_x  =  32 * 90 -320;
	}
}
