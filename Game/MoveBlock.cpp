//----------------------------------------------------------------------
Moveblock::Moveblock()
{
	g_gimmickimage = new Texture(L"Resources\\Images\\orange.png");

	m_handle = g_gimmickimage;
	m_grp_x = 0;
	m_grp_y = 0;
	m_grp_w = m_block_size * 3;
	m_grp_h = m_block_size * 3;
	m_pos_x = m_block_size * 10;
	m_pos_y = 0;
}

//----------------------------------------------------------------------
//! @brief デストラクタ
//!
//----------------------------------------------------------------------
Moveblock::~Moveblock()
{
	delete g_gimmickimage;
}


//----------------------------------------------------------------------
//! @brief ギミックの上下移動
//!
//! @return なし
//----------------------------------------------------------------------
void Moveblock::Move_up_down()
{
	if (m_pos_y_min == m_pos_y)
	{
		SetSpdY(6);
	}
	else if (m_pos_y_max == m_pos_y)
	{
		SetSpdY(-6);
	}
}


